function initMap() {
    navigator.geolocation.getCurrentPosition(function(position) {
        let map = new google.maps.Map(document.getElementById('map'), {
                center: {
                    lat: position.coords.latitude,
                    lng: position.coords.longitude
                },
                scrollwheel: true,
                zoom: 17
            });
      map.setOptions({styles: mapStyle});
      fbase.get('geo_alerts', 'value', (snapshot) => {
            for (var snap of Object.keys(snapshot).map(key => snapshot[key])) {
                var infowindow = new google.maps.InfoWindow({
                    content: '<div id="content">'+
                        '<div id="siteNotice"></div>'+
                        '<h4 id="firstHeading" class="firstHeading">Test titre</h4>'+
                        '<div id="bodyContent">'+
                        '<p>' + snap.title + '</p>'+
                        '</div></div>'
                });
                var marker = new google.maps.Marker({
                    map: map,
                    position: {
                        lat: snap.lat,
                        lng: snap.lng
                    },
                    title: snap.title
                });
                marker.addListener('click', function() {
                    infowindow.open(map, marker);
                });
            }
        });
    });
}

function sendPosition() {
    var input = document.getElementById('formInput');
    if (!input.value) {
        alert("Nous n'avons détecté aucune information complémentaire concernant l'alerte envoyée. Veuillez remplir le champ approprié.");
    } else {
        navigator.geolocation.getCurrentPosition(function(position) {
            var geoAlert = {
                lat: position.coords.latitude,
                lng: position.coords.longitude,
                title: input.value
            };
            fbase.push ('geo_alerts', geoAlert);
            input.value = '';
            alert("Merci de nous avoir prévenu d'un besoin.")
        });
    }
}
